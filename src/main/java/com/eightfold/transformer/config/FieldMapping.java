package com.eightfold.transformer.config;

public class FieldMapping {

    private String path;

    private String from;

    public FieldMapping() {
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getFrom() {
        return from;
    }

    public void setFrom(String from) {
        this.from = from;
    }
}